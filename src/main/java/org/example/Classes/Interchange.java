package org.example.Classes;

public class Interchange {
    int id_interchange, id_driver, id_conductor, id_route_sheet;

    public int getId_interchange() {
        return id_interchange;
    }

    public void setId_interchange(int id_interchange) {
        this.id_interchange = id_interchange;
    }

    public int getId_driver() {
        return id_driver;
    }

    public void setId_driver(int id_driver) {
        this.id_driver = id_driver;
    }

    public int getId_conductor() {
        return id_conductor;
    }

    public void setId_conductor(int id_conductor) {
        this.id_conductor = id_conductor;
    }

    public int getId_route_sheet() {
        return id_route_sheet;
    }

    public void setId_route_sheet(int id_route_sheet) {
        this.id_route_sheet = id_route_sheet;
    }
}
